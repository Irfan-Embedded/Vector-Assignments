
/*
 * 06. Write a program to input an integer number in decimal number system, and show the 
 *     equivalent output in hexadecimal and octal format.
 *
 */

#include <stdio.h>

int main()
{
	int num;

	printf("Program to see the equivalent output in hexadecimal and octal format\n\n");

	printf("Enter any num : ");
	scanf("%d",&num);

	printf("octal value of given number is : %o\n",num );
	printf("Hexadecimal value of given number is : %x\n",num );
}


